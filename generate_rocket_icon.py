from PIL import Image, ImageDraw
size = 256
img = Image.new('RGBA', (size, size), (0, 0, 0, 0))
d = ImageDraw.Draw(img)
body = [(110, 50), (146, 50), (170, 120), (170, 180), (86, 180), (86, 120)]
d.polygon(body, fill=(255, 255, 255, 255))
d.ellipse((100, 55, 156, 120), fill=(255, 255, 255, 255))
# nose
n = [(118, 50), (138, 18), (158, 50)]
d.polygon(n, fill=(255, 80, 80, 255))
# fins
d.polygon([(86, 120), (60, 140), (86, 160)], fill=(80, 80, 255, 255))
d.polygon([(170, 120), (196, 140), (170, 160)], fill=(80, 80, 255, 255))
# window
d.ellipse((118, 90, 146, 118), fill=(102, 179, 255, 255))
# flames
d.polygon([(110, 180), (146, 180), (160, 240), (138, 230), (124, 240)], fill=(255, 180, 50, 255))
d.polygon([(120, 180), (136, 180), (142, 220), (128, 220)], fill=(255, 80, 80, 255))
# stars
for x, y in [(28, 30), (210, 40), (34, 200), (230, 210), (190, 86)]:
    d.ellipse((x, y, x+10, y+10), fill=(255, 255, 255, 255))
img.save('us.ico', format='ICO', sizes=[(256, 256), (128, 128), (64, 64), (48, 48), (32, 32)])
